package net.java.registration.model;

public class Employee 
{
	private String NAME;
    private String D_O_B;
    private String GENDER;
    private String YEARS_OF_EXPERIENCE;
    private String SKILL_SET;
    private String SUMMARY;
   
	public String getNAME() {
		return NAME;
	}
	public void setNAME(String nAME) {
		this.NAME = nAME;
	}
	public String getD_O_B() {
		return D_O_B;
	}
	public void setD_O_B(String d_O_B) {
		this.D_O_B = d_O_B;
	}
	public String getGENDER() {
		return GENDER;
	}
	public void setGENDER(String gENDER) {
		this.GENDER = gENDER;
	}
	public String getYEARS_OF_EXPERIENCE() {
		return YEARS_OF_EXPERIENCE;
	}
	public void setYEARS_OF_EXPERIENCE(String yEARS_OF_EXPERIENCE) {
		this.YEARS_OF_EXPERIENCE = yEARS_OF_EXPERIENCE;
	}
	public String getSKILL_SET() {
		return SKILL_SET;
	}
	public void setSKILL_SET(String sKILL_SET) {
		this.SKILL_SET = sKILL_SET;
	}
	public String getSUMMARY() {
		return SUMMARY;
	}
	public void setSUMMARY(String sUMMARY) {
		this.SUMMARY = sUMMARY;
	}
    
    
    
}

